/*

<자릿수 더하기>

문제 설명
자연수 N이 주어지면, N의 각 자릿수의 합을 구해서 
return 하는 solution 함수를 만들어 주세요.
예를들어 N = 123이면 1 + 2 + 3 = 6을 return 하면 됩니다.

제한사항
N의 범위 : 100,000,000 이하의 자연수

입출력 예
N			answer
123			6
987			24

입출력 예 설명
입출력 예 #1
문제의 예시와 같습니다.

입출력 예 #2
9 + 8 + 7 = 24이므로 24를 return 하면 됩니다.

*/

// 새로운 풀이1 (정수를 string 타입으로 바꾼 뒤 각 자리마다 int로 바꾸어 더하기) 
int solution1(int n)
{
	int answer = 0;
	string str = to_string(n);

	for (int i = 0; i < str.size(); i++)
	{
		answer += (str[i] - '0');
	}

	return answer;
}

// 새로운 풀이2
int solution2(int n)
{
	int answer = 0;

	while (n > 0)
	{
		answer += n % 10;
		n /= 10;
	}

	return answer;
}

// 최초의 풀이 
int solution(int n)
{
	int answer = 0;

	for (int i = 1; i <= n; i *= 10)
	{
		answer += (n / i) % 10;
	}

	return answer;
}

